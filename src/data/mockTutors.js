/**
 * Get all mock tutors
 */
export const getMockTutors = () => {
  return mockTutors;
};

/**
 * Get tutor by ID
 */
export const getMockTutorById = (id) => {
  return mockTutors.find(tutor => tutor.id === id);
};

/**
 * Filter tutors by criteria
 */
export const filterMockTutors = (filters = {}) => {
  let filtered = [...mockTutors];
  
  if (filters.subject) {
    filtered = filtered.filter(tutor => 
      tutor.subjects.some(s => s.toLowerCase().includes(filters.subject.toLowerCase()))
    );
  }
  
  if (filters.class) {
    filtered = filtered.filter(tutor => 
      tutor.classes.includes(filters.class)
    );
  }
  
  if (filters.area) {
    filtered = filtered.filter(tutor => 
      tutor.areas.includes(filters.area)
    );
  }
  
  if (filters.verified !== undefined) {
    filtered = filtered.filter(tutor => tutor.verified === filters.verified);
  }
  
  if (filters.gender) {
    filtered = filtered.filter(tutor => tutor.gender === filters.gender);
  }
  
  return filtered;
};

/**
 * Store mock tutors in localStorage (for demo)
 */
export const saveMockTutorsToLocalStorage = () => {
  localStorage.setItem('tutors', JSON.stringify(mockTutors));
};

/**
 * Load tutors from localStorage
 */
export const loadTutorsFromLocalStorage = () => {
  const stored = localStorage.getItem('tutors');
  return stored ? JSON.parse(stored) : mockTutors;
};
